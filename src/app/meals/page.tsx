    const trimmed = newGroceryItem.trim();
    if (!trimmed) return;
    const catDef = groceryCategories.find(c => c.id === newGroceryCategory);
    db.upsertGroceryItem({ name: trimmed, category: newGroceryCategory, aisle: "1", quantity: "", notes: "", priority: newGroceryPriority, needed: true, source: "manual", autoGenerated: false });
    const fresh = db.selectGrocery().map(mapDbToGrocery);
