  const [syncStatus, setSyncStatus] = useState("");

  // Recipe Creator
  const [showRecipeModal, setShowRecipeModal] = useState(false);
  const [editingMealId, setEditingMealId] = useState<number | null>(null);
  const [recipe, setRecipe] = useState({ ...emptyRecipe });
  const [showEmojiPicker, setShowEmojiPicker] = useState(false);

  // AI meal suggestions
  const [aiMealIdeas, setAiMealIdeas] = useState<Array<{ name: string; emoji: string; tags: string[] }>>([]);
  const [aiMealLoading, setAiMealLoading] = useState(false);
  const [showAiSuggestions, setShowAiSuggestions] = useState(false);

  const generateAiMeals = async () => {
    setAiMealLoading(true);
    setShowAiSuggestions(true);
    try {
      const pantry = db.selectPantry().map((p: any) => p.name).join(", ");
      const res = await fetch('/api/hermes/chat', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          message: `Suggest 4 meal ideas for a family of 7 (kids ages 5-14). Pantry has: ${pantry || "basic ingredients"}. Return as JSON: {"actions":[{"type":"meal","title":"Meal Name","detail":"Prep time · Kid-friendly tags","emoji":"🍝"}]}. Make them varied, practical, and family-friendly.`,
        }),
      });
      const data = await res.json();
      const actions = data.actions || [];
      const ideas = actions
        .filter((a: any) => a.type === "meal")
        .map((a: any) => ({
          name: a.title,
          emoji: a.emoji || "🍽️",
          tags: a.detail?.split("·").map((t: string) => t.trim()).filter(Boolean) || ["Family"],
        }));
      setAiMealIdeas(ideas.length > 0 ? ideas : mealIdeas);
    } catch {
      setAiMealIdeas(mealIdeas);
    }
    setAiMealLoading(false);
  };

  // Pantry
  const [pantryItems, setPantryItems] = useState<PantryItem[]>([]);
  const [newPantryItem, setNewPantryItem] = useState("");
  const [newPantryStatus, setNewPantryStatus] = useState<"plenty" | "low" | "out">("plenty");

  // Grocery
  const [groceryItems, setGroceryItems] = useState<GroceryItem[]>(initialGroceryItems);
  const [activeCategory, setActiveCategory] = useState("all");
  const [newGroceryItem, setNewGroceryItem] = useState("");
  const [newGroceryCategory, setNewGroceryCategory] = useState("pantry");
  const [newGroceryPriority, setNewGroceryPriority] = useState<"low" | "medium" | "high">("medium");
  const [editingGroceryId, setEditingGroceryId] = useState<number | null>(null);
  const [editName, setEditName] = useState("");
  const [editQuantity, setEditQuantity] = useState("");
  const [editNotes, setEditNotes] = useState("");

  // localStorage helpers
  const MEALS_KEY = "consuela-meals";
  const PANTRY_KEY = "consuela-pantry";
  const GROCERY_KEY = "consuela-grocery";
  const loadJSON = <T,>(key: string, fallback: T): T => {
    if (typeof window === "undefined") return fallback;
    try { const s = localStorage.getItem(key); return s ? JSON.parse(s) : fallback; }
    catch { return fallback; }
  };

  useEffect(() => {
    const loaded = db.selectMeals();
    const mealDefaults = loaded.length ? loaded : defaultMeals;
    // Check localStorage first
    const saved = loadJSON(MEALS_KEY, mealDefaults);
    setMeals(saved);
    const pantry = db.selectPantry().map((p: any) => ({ id: p.id, item: p.name, status: p.status }));
    const savedPantry = loadJSON(PANTRY_KEY, pantry.length ? pantry : []);
    setPantryItems(savedPantry);
    const grocery = db.selectGrocery();
    const defaultGrocery = grocery.length ? grocery.map(mapDbToGrocery) : initialGroceryItems;
    const savedGrocery = loadJSON(GROCERY_KEY, defaultGrocery);
    setGroceryItems(savedGrocery);
  }, []);

  // Persist on change
  useEffect(() => { if (meals.length) localStorage.setItem(MEALS_KEY, JSON.stringify(meals)); }, [meals]);
  useEffect(() => { if (pantryItems.length) localStorage.setItem(PANTRY_KEY, JSON.stringify(pantryItems)); }, [pantryItems]);
  useEffect(() => { if (groceryItems.length) localStorage.setItem(GROCERY_KEY, JSON.stringify(groceryItems)); }, [groceryItems]);

