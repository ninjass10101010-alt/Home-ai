import PocketBase from 'pocketbase';

const pbUrl = process.env.NEXT_PUBLIC_PB_URL || 'http://192.168.0.27:8091';
const pb = new PocketBase(pbUrl);

async function main() {
  console.log(`Connecting to PocketBase at ${pbUrl}...`);
  try {
    await pb.health.check();
    console.log("✅ PocketBase is reachable!");
  } catch (err) {
    console.error("❌ PocketBase is not reachable. Make sure the Docker container is running.");
    process.exit(1);
  }

  console.log("\nAuthenticating with PocketBase admin...");

  const email = process.env.PB_ADMIN_EMAIL || 'admin@consuela.app';
  const password = process.env.PB_ADMIN_PASSWORD || 'admin123456';

  try {
    await pb.admins.authWithPassword(email, password);
    console.log("✅ Authenticated successfully!");
  } catch (err) {
    console.error("❌ Authentication failed. Please set PB_ADMIN_EMAIL and PB_ADMIN_PASSWORD environment variables.");
    console.error("Or create an admin account in the PocketBase UI at http://192.168.0.27:8091/_/");
    process.exit(1);
  }

  console.log("\nExtending schema for smart meal planning system...");

  // New collections for the architecture
  const newCollections = [
    {
      name: "ingredients",
      type: "base",
      schema: [
        { name: "name", type: "text", required: true }, // normalized name
        { name: "category", type: "select", options: { values: ["produce", "dairy", "meat", "pantry", "frozen", "snacks", "beverages", "household"] }, required: true },
        { name: "unit", type: "select", options: { values: ["g", "kg", "ml", "l", "count", "cup", "tbsp", "tsp", "oz", "lb"] }, required: true },

        // AI/ML ready fields
        { name: "embeddingVector", type: "json" }, // for semantic search
        { name: "nutritionPerUnit", type: "json" }, // {calories, protein, carbs, fat, fiber, sugar, sodium}
        { name: "commonSubstitutes", type: "json" }, // array of ingredient IDs
        { name: "seasonality", type: "json" }, // {peakMonths: [1-12]}
        { name: "storageTips", type: "text" },
        { name: "allergyTags", type: "json" }, // ['gluten', 'nuts', ...]
      ]
    },
    {
      name: "recipes",
      type: "base",
      schema: [
        { name: "name", type: "text", required: true },
        { name: "instructions", type: "json", required: true }, // array of steps
        { name: "cuisineType", type: "json" }, // array of types
        { name: "dietaryTags", type: "json" }, // ['vegetarian', 'gluten-free', ...]
        { name: "prepTimeMinutes", type: "number" },
        { name: "cookTimeMinutes", type: "number" },
        { name: "difficulty", type: "select", options: { values: ["easy", "medium", "hard"] } },

        // AI-ready fields
        { name: "flavorProfile", type: "json" }, // sweet/salty scores
        { name: "pairingSuggestions", type: "json" }, // recipe IDs
        { name: "imageUrl", type: "text" },
      ]
    },
    {
      name: "recipe_ingredients",
      type: "base",
      schema: [
        { name: "recipeId", type: "relation", options: { collectionId: "recipes", maxSelect: 1 }, required: true },
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 }, required: true },
        { name: "quantity", type: "number", required: true },
        { name: "unit", type: "text", required: true },
        { name: "notes", type: "text" }, // "finely chopped", etc.
      ]
    },
    {
      name: "meal_plan_entries",
      type: "base",
      schema: [
        { name: "recipeId", type: "relation", options: { collectionId: "recipes", maxSelect: 1 }, required: true },
        { name: "scheduledFor", type: "date", required: true }, // date
        { name: "servings", type: "number", required: true, default: 4 },
        { name: "notes", type: "text" },
        { name: "autoGenerated", type: "bool", default: false },
        { name: "lastSyncedAt", type: "date" },
      ]
    },
    {
      name: "pantry_items_extended",
      type: "base",
      schema: [
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 }, required: true },
        { name: "quantity", type: "number", required: true },
        { name: "unit", type: "text", required: true },
        { name: "location", type: "text" }, // 'fridge', 'pantry-shelf2'
        { name: "expirationDate", type: "date" },
        { name: "purchaseDate", type: "date" },
        { name: "pricePaid", type: "number" },

        // AI-ready fields
        { name: "predictedExpiration", type: "date" },
        { name: "confidenceScore", type: "number" }, // 0-1
      ]
    },
    {
      name: "grocery_list_items_extended",
      type: "base",
      schema: [
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 }, required: true },
        { name: "quantityNeeded", type: "number", required: true },
        { name: "unit", type: "text", required: true },
        { name: "priority", type: "select", options: { values: ["low", "medium", "high"] }, default: "medium" },
        { name: "source", type: "select", options: { values: ["meal-plan", "pantry-low", "manual"] }, default: "manual" },
        { name: "addedFromMealPlanId", type: "relation", options: { collectionId: "meal_plan_entries", maxSelect: 1 } },
        { name: "addedAt", type: "date", required: true },
        { name: "purchasedAt", type: "date" },

        // AI-ready fields
        { name: "suggestedStoreSection", type: "text" },
        { name: "priceEstimate", type: "number" },
        { name: "isFavorite", type: "bool", default: false },
      ]
    },
    {
      name: "purchase_history",
      type: "base",
      schema: [
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 }, required: true },
        { name: "quantity", type: "number", required: true },
        { name: "unit", type: "text", required: true },
        { name: "purchaseDate", type: "date", required: true },
        { name: "pricePaid", type: "number", required: true },
        { name: "store", type: "text" },

        // For "Recently Purchased" UX
        { name: "displayPriority", type: "number", default: 0 },
      ]
    }
  ];

  console.log("\nCreating new collections...");

  for (const collection of newCollections) {
    try {
      await pb.collections.create(collection);
      console.log(`✅ Created collection: ${collection.name}`);
    } catch (err) {
      if (err.status === 400 && err.response?.data?.name?.code === 'validation_invalid_format') {
        console.log(`⚠️ Collection ${collection.name} might already exist or has invalid config.`);
      } else {
        console.error(`❌ Failed to create ${collection.name}:`, err.response?.data || err.message);
      }
    }
  }

  // Extend existing collections with new fields
  console.log("\nExtending existing collections...");

  const collectionUpdates = [
    {
      collectionName: "meals",
      newFields: [
        { name: "recipeId", type: "relation", options: { collectionId: "recipes", maxSelect: 1 } },
        { name: "servings", type: "number", default: 4 },
        { name: "autoGenerated", type: "bool", default: false },
        { name: "lastSyncedAt", type: "date" },
      ]
    },
    {
      collectionName: "grocery_items",
      newFields: [
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 } },
        { name: "quantityNeeded", type: "number", default: 1 },
        { name: "unit", type: "text", default: "count" },
        { name: "priority", type: "select", options: { values: ["low", "medium", "high"] }, default: "medium" },
        { name: "source", type: "select", options: { values: ["meal-plan", "pantry-low", "manual"] }, default: "manual" },
        { name: "addedFromMealPlanId", type: "relation", options: { collectionId: "meal_plan_entries", maxSelect: 1 } },
        { name: "addedAt", type: "date", required: true },
        { name: "purchasedAt", type: "date" },
        { name: "suggestedStoreSection", type: "text" },
        { name: "priceEstimate", type: "number" },
        { name: "isFavorite", type: "bool", default: false },
      ]
    },
    {
      collectionName: "pantry_items",
      newFields: [
        { name: "ingredientId", type: "relation", options: { collectionId: "ingredients", maxSelect: 1 } },
        { name: "quantity", type: "number", default: 1 },
        { name: "unit", type: "text", default: "count" },
        { name: "location", type: "text" },
        { name: "expirationDate", type: "date" },
        { name: "purchaseDate", type: "date" },
        { name: "pricePaid", type: "number" },
        { name: "predictedExpiration", type: "date" },
        { name: "confidenceScore", type: "number" },
      ]
    }
  ];

  for (const update of collectionUpdates) {
    try {
      const collection = await pb.collections.getFirstListItem(`name="${update.collectionName}"`);

      for (const field of update.newFields) {
        try {
          await pb.collections.update(collection.id, {
            ...collection,
            schema: [...collection.schema, field]
          });
          console.log(`✅ Added field ${field.name} to ${update.collectionName}`);
        } catch (fieldErr) {
          console.log(`⚠️ Field ${field.name} might already exist in ${update.collectionName}`);
        }
      }
    } catch (err) {
      console.error(`❌ Failed to update ${update.collectionName}:`, err.response?.data || err.message);
    }
  }

  console.log("\n🎉 Smart meal planning schema extension complete!");
  console.log("\nNext steps:");
  console.log("1. Seed some ingredients and recipes");
  console.log("2. Update the frontend to use the new collections");
  console.log("3. Implement the sync logic");

  process.exit(0);
}

main();